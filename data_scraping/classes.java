package web_scraper;

public class classes {
	private String id;
	private String className;
	private String professor;
	private String day;
	private String time;
	private String category;
	
	public classes(String id, String className, String professor, String day, String time, String category) {
		this.id = id;
		this.className = className;
		this.professor = professor;
		this.day = day;
		this.time = time;
		this.category = category;
	}
}
